public class doWhile {
    public static void main(String arg[]){
        //do while loop :- first run then check
        //syntax->  do{body}while(condition);
        // eg. print "shezzy 10 times using do while"
        int count = 1;
        do{
            System.out.print("Shezzy\n");
            count++;
        }while(count<=10);
    }
}
